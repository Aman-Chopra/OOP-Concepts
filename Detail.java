public class Detail
{
public String Name;
public String Address;
public long telno;
public double rent;
public Detail(String s,String a,long t,double r)
{
Name=s;
Address=a;
telno=t;
rent=r;
}
public void show()
{
System.out.println("Name:- "+Name);
System.out.println("Address:- "+Address);
System.out.println("Telephone no.:- "+telno);
System.out.println("Rent:- "+rent);
}
}
